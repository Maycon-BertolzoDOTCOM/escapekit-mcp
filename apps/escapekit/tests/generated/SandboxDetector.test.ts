import { describe, it, expect } from 'vitest';
import { SandboxDetector, createSandboxDetector } from '../../src/detectors/SandboxDetector.ts';

describe('SandboxDetector', () => {
  describe('detect', () => {
    it('should detect Google AI Studio', () => {
      const detector = createSandboxDetector();
      const code = 'import { GenerativeModel } from "@google/generative-ai";';
      const result = detector.detect(code);
      expect(result).toBe('ai-studio');
    });

    it('should detect Bolt.new', () => {
      const detector = createSandboxDetector();
      const code = '// generated by bolt.new';
      const result = detector.detect(code);
      expect(result).toBe('bolt.new');
    });

    it('should detect Replit', () => {
      const detector = createSandboxDetector();
      const code = 'import { auth } from "@replit/auth";';
      const result = detector.detect(code);
      expect(result).toBe('replit');
    });

    it('should detect CodeSandbox', () => {
      const detector = createSandboxDetector();
      const code = '// codesandbox.io project';
      const result = detector.detect(code);
      expect(result).toBe('codesandbox');
    });

    it('should detect StackBlitz', () => {
      const detector = createSandboxDetector();
      const code = '// stackblitz.io project';
      const result = detector.detect(code);
      expect(result).toBe('stackblitz');
    });

    it('should return unknown for no sandbox', () => {
      const detector = createSandboxDetector();
      const code = 'const x = 1;';
      const result = detector.detect(code);
      expect(result).toBe('unknown');
    });
  });

  describe('detectAll', () => {
    it('should detect multiple sandbox patterns', () => {
      const detector = createSandboxDetector();
      const code = 'import { Gemini } from "@google/generative-ai"; // codesandbox';
      const result = detector.detectAll(code);
      expect(result.length).toBeGreaterThanOrEqual(1);
    });
  });

  describe('isFromSandbox', () => {
    it('should return true for AI Studio code', () => {
      const detector = createSandboxDetector();
      const code = 'import { GenerativeModel } from "@google/generative-ai";';
      expect(detector.isFromSandbox(code, 'ai-studio')).toBe(true);
    });

    it('should return false for wrong sandbox', () => {
      const detector = createSandboxDetector();
      const code = 'const x = 1;';
      expect(detector.isFromSandbox(code, 'replit')).toBe(false);
    });
  });

  describe('getConfidence', () => {
    it('should return confidence for AI Studio', () => {
      const detector = createSandboxDetector();
      const code = 'import { GenerativeModel } from "@google/generative-ai";';
      const confidence = detector.getConfidence(code, 'ai-studio');
      expect(confidence).toBeGreaterThan(0);
    });

    it('should return 0 for no match', () => {
      const detector = createSandboxDetector();
      const code = 'const x = 1;';
      const confidence = detector.getConfidence(code, 'replit');
      expect(confidence).toBe(0);
    });
  });

  describe('detectDetailed', () => {
    it('should return detailed detection results', () => {
      const detector = createSandboxDetector();
      const code = 'import { GenerativeModel } from "@google/generative-ai";';
      const results = detector.detectDetailed(code);
      expect(results.length).toBeGreaterThan(0);
      expect(results[0].type).toBe('ai-studio');
      expect(results[0].description).toBeDefined();
    });
  });
});
