/**
 * XML/HTML Escaping
 *
 * Safe interpolation of untrusted strings.
 * Inspired by Claude Code's utils/xml.ts.
 */

/**
 * Escape a string for safe inclusion in XML/HTML content
 */
export function escapeXml(str: string): string {
  return str
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&apos;');
}

/**
 * Escape a string for safe inclusion in an XML/HTML attribute
 */
export function escapeXmlAttr(str: string): string {
  return str
    .replace(/&/g, '&amp;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&apos;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;');
}
